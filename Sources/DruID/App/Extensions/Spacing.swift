    static let spacingXS: CGFloat = 8
    /// 16 pixels spacing
    static let spacingS: CGFloat = 12
    /// 24 pixels spacing
    static let spacingM: CGFloat = 16
    /// 32 pixels spacing
    static let spacingL: CGFloat = 20
    /// 40 pixels spacing
    static let spacingXL: CGFloat = 24
    /// 48 pixels spacing
    static let spacingXXL: CGFloat = 32
}
